        },

        methods: {
            loadDogs: function() {
                axios.get('/api/v1/dogs')
                .then((response) => {
                    console.log(response.data);
                    this.dogs = response.data.data;
                })
                .catch( function(error) {
                    console.error(error);
                });
            },

            deleteDog: async function(id) {
                axios.delete('/api/v1/dogs/delete/'+id)
                .then( (response) => {
                    console.log(response);

                    let index = this.dogs.findIndex(dog => dog.id === id);
                    this.dogs.splice(index, 1);
                })
                .catch( function(error) {
                    console.error(error);
                });
            }
        }
    }
